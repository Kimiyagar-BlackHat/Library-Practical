<?php
	class _FILE
	{
        public $InputData;
        public function __construct($Data)
        {
            $this->InputData = $Data;
            return $this->InputData;
        }        
//%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
		public function _Get()
		{
		    if($this->_Check() && $this->_IsSet())	
                return $this->InputData;
            return FALSE;
		}
//------------------------------------------------------------------------------------------------------------------
		public function _Check()
		{
			if(	$this->IsDir() 			||
				$this->IsExecutable() 	||
				$this->IsLink() 		||
				$this->IsFile() 		||
				$this->IsReadable() 	||
				$this->IsWritable() 	||
				$this->IsUploadedFile()
            )
            return TRUE;
			return FALSE;
		}
//------------------------------------------------------------------------------------------------------------------
		public function _Empty()
		{
			if($this->_Check() && $this->_IsFull())
            {
            	$Write = $this->WriteFile($this->InputData , 'w' , '');
            	if($Write)
                    return TRUE;
            	else
                    return FALSE;
            }
            return FALSE;
		}
//------------------------------------------------------------------------------------------------------------------		
		public function _IsSet()
		{
			if(isset($this->InputData) && $this->_IsFull())
                return TRUE;
            return FALSE;
		}
//------------------------------------------------------------------------------------------------------------------
		public function _IsEmpty()
		{
			clearstatcache();
			if(!filesize($this->InputData))								
                return TRUE;
            return FALSE;
		}
//------------------------------------------------------------------------------------------------------------------
		public function _IsFull()
		{
			if(!$this->_IsEmpty())							
                return TRUE;
            return FALSE;
		}
//%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
		public function IsDir()
        {
            if(is_dir($this->InputData)) 									
                return TRUE;
            return FALSE;
        }
//------------------------------------------------------------------------------------------------------------------
        public function IsExecutable()
        {
            if(is_executable($this->InputData)) 							
                return TRUE;
            return FALSE;
        }        
//------------------------------------------------------------------------------------------------------------------
        public function IsLink()
        {
            if(is_link($this->InputData)) 									
                return TRUE;
            return FALSE;
        }        
//------------------------------------------------------------------------------------------------------------------
        public function IsFile()
        {
            if(is_file($this->InputData)) 									
                return TRUE;
            return FALSE;
        }        
//------------------------------------------------------------------------------------------------------------------
        public function IsReadable()
        {
            if(is_readable($this->InputData)) 								
                return TRUE;
            return FALSE;
        }        
//------------------------------------------------------------------------------------------------------------------
        public function IsWritable()
        {
            if(is_writable($this->InputData)) 								
                return TRUE;
            return FALSE;
        }        
//------------------------------------------------------------------------------------------------------------------
        public function IsUploadedFile()
        {
            if(is_uploaded_file($this->InputData))							
                return TRUE;
			return FALSE;
        }        
//%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
        public function WriteFile($FileName , $Method , $InputData)
        {
            global $Write , $Close;
        	$Open = self::OpenFile($FileName , $Method);
        	if($Open)
			{
				$Write = fwrite($Open , $InputData);
				$Close = self::CloseFile($Open);
				return TRUE;
			}
			return FALSE;
        }
//------------------------------------------------------------------------------------------------------------------
        public function OpenFile($FileName , $Method)
        {
        	if($this->IsWritable() && $this->IsReadable())
        	{
        		return fopen($FileName, $Method);
        	}
        	return FALSE;
        }
//------------------------------------------------------------------------------------------------------------------
       	public function CloseFile($File)
       	{
       		return fclose($File);
       	}
